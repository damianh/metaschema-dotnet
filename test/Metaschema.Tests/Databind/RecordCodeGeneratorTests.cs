        // Assert
        var assemblyFile = files.First(f => f.Key.Contains("Document.g.cs"));
        assemblyFile.Value.ShouldContain("namespace MyApp.Models");
        assemblyFile.Value.ShouldContain("{");
        // Should have closing brace for namespace
        assemblyFile.Value.Count(c => c == '{').ShouldBeGreaterThan(0);
