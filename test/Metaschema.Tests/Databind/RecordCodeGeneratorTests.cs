    #region Namespace Tests

    [Fact]
    public void Generate_WithFileScopedNamespaces_ShouldUseFileScopedSyntax()
    {
        // Arrange
        var module = LoadTestModule();
        var options = new CodeGenerationOptions 
        { 
            Namespace = "MyApp.Models",
            FileScopedNamespaces = true 
        };
        var generator = new RecordCodeGenerator(options);

        // Act
        var files = generator.Generate(module);

        // Assert
        var assemblyFile = files.First(f => f.Key.Contains("Document.g.cs"));
        assemblyFile.Value.ShouldContain("namespace MyApp.Models;");
        assemblyFile.Value.ShouldNotContain("namespace MyApp.Models\n{");
    }

    [Fact]
    public void Generate_WithBlockScopedNamespaces_ShouldUseBlockSyntax()
    {
        // Arrange
        var module = LoadTestModule();
        var options = new CodeGenerationOptions 
        { 
            Namespace = "MyApp.Models",
            FileScopedNamespaces = false 
        };
        var generator = new RecordCodeGenerator(options);

